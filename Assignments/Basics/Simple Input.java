import java.util.*;
public class Main {
    public static void main(String args[]) {
      
      Scanner scn = new Scanner(System.in);
      int sol=0;

      while(sol>=0){

          int num = scn.nextInt();
          sol = sol+num;

          if(sol>=0){
              System.out.println(num);
          }

      }



    }
}
